public class LocDataEntry
{
	public readonly string English;

	public readonly string French;

	public readonly string Italian;

	public readonly string German;

	public readonly string SpanishSpain;

	public readonly string PortugueseBrazil;

	public readonly string Russian;

	public LocDataEntry(string english, string french, string italian, string german, string spanishSpain, string portugueseBrazil, string russian)
	{
		English = english;
		French = french;
		Italian = italian;
		German = german;
		SpanishSpain = spanishSpain;
		PortugueseBrazil = portugueseBrazil;
		Russian = russian;
	}
}
